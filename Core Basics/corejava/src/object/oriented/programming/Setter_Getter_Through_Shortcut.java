package object.oriented.programming;

public class Setter_Getter_Through_Shortcut {
	private String color;
	private int Width;
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public int getWidth() {
		return Width;
	}
	public void setWidth(int width) {
		Width = width;
	}

}
