package basicjava;

public class Multipleifelse {
	public static void main(String[] args)
	{
	int rs=1000;
	if(rs<107) {
		System.out.println("You can buy less than one litre petrol");
	}
	 else if(rs==107) {
		System.out.println("You can buy one litre petrol");
	}
	else {
		System.out.println("You can buy more than one litre petrol");
		
	}
	}

}
